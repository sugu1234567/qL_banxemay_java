    private void GetDataTable() throws SQLException{
        
        try {
            sqlconn = DriverManager.getConnection(data,username,pass);
            pst = sqlconn.prepareStatement("select * from nhanvien order by IDNhanVien,HoTen");
            rs = pst.executeQuery();

            ResultSetMetaData stData = rs.getMetaData();
            q= stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            while (rs.next()){
            Vector columnData = new Vector();
            for (i=1; i<=q; i++){
                columnData.add(rs.getString("IDNhanVien"));
                columnData.add(rs.getString("HoTen"));
                columnData.add(rs.getString("ChucVu"));
                columnData.add(rs.getString("SDT"));
                columnData.add(rs.getString("Email"));                
            }
            RecordTable.addRow(columnData);
        }
        
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }
